package com.fuzzypg;

/**
 * 
 * 
 * @author Clayton
 */
public class Pair {
    
    private final double first;
    private final double second;

    public Pair(double first, double second) {
        this.first = first;
        this.second = second;
    }
    
    public double getFirst() {
        return first;
    }
    
    public double getSecond() {
        return second;
    }
}
